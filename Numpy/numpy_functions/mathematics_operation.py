import numpy as np

arry = np.array([20, 22, 23, 40, 46, 59, 54, 67])

square = np.sqrt(arry)
mean = np.mean(arry)
median = np.median(arry)
maximum = np.max(arry)
minimum = np.min(arry)

print(f"The squares are {square}")
print(f"The mean is {mean}")
print(f"The median is {median}")

print(f"The maximum is {maximum}")
print(f"The minimum is {minimum}")